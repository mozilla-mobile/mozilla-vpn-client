function getAssetSource(assetName, assetLookup) {
  const assetData = assetLookup[assetName];
  if (assetData === undefined) {
    console.error('Unable to find asset data for: ' + assetName)
    return
  }
  const potentialName = MZTheme.colors.useDarkAssets ? assetData.filenameDark :
                                                       assetData.filenameLight
  if (potentialName === undefined) {
    console.error('Unable to find asset information for: ' + assetName)
    return
  }
  return potentialName
}

function getImageSource(imageName) {
  return getAssetSource(imageName, imageLookup)
}

